class Product {
  final int id;
  final String name;
  final String category;
  final String image;
  final String describtion;
  final int price;
  int quantity;
  Product({
    required this.id,
    required this.name,
    required this.category,
    required this.image,
    required this.describtion,
    required this.price,
    required this.quantity,
  });
}
